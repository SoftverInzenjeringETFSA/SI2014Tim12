package ba.unsa.etf.si.tim12;

public class MaterijalNotFound extends Exception {
	public MaterijalNotFound() {
		super();
		
	}

	public MaterijalNotFound(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public MaterijalNotFound(String message, Throwable cause) {
		super(message, cause);
		
	}

	public MaterijalNotFound(String message) {
		super(message);
		
	}

	public MaterijalNotFound(Throwable cause) {
		super(cause);
		
	}
}
