package ba.unsa.etf.si.tim12;

public class PacijentNotFound extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5841301188054242661L;

	public PacijentNotFound() {
		super();
		
	}

	public PacijentNotFound(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public PacijentNotFound(String message, Throwable cause) {
		super(message, cause);
		
	}

	public PacijentNotFound(String message) {
		super(message);
		
	}

	public PacijentNotFound(Throwable cause) {
		super(cause);
		
	}
	
}
